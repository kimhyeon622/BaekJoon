import java.util.*;

public class Main {

	public static void main(String[] args){
		// 입출력에 사용할 객체
        Scanner sc = new Scanner(System.in);
        
        // 정수 A와B 입력 받기
        int A = sc.nextInt();
        int B = sc.nextInt();
        
        // A와B를 더한 값 출력
        System.out.println(A+B);
	}
}